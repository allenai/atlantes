import numpy as np


def get_world_partitions(n_world_partitions: int = 8) -> dict:
    """_summary_

    Parameters
    ----------
    n_world_partitions : int, optional
        _description_, by default 8

    Returns
    -------
    _type_
        _description_
    """
    lons = np.linspace(-179, 179, num=n_world_partitions)
    lats = np.linspace(-80, 80, num=int(n_world_partitions / 2))
    quad = 0
    world_dict = {}
    for index_lon in range(len(lons) - 1):
        for index_lat in range(len(lats) - 1):
            world_dict[f"quad_{quad}"] = (
                (lons[index_lon], lons[index_lon + 1]),
                (lats[index_lat], lats[index_lat + 1]),
            )
            quad += 1

    return world_dict


